"use client";

import Link from "next/link";
import { usePathname } from "next/navigation";

const tabs = [
  { href: "/", label: "Dashboard", icon: DashboardIcon },
  { href: "/shopping", label: "Zakupy", icon: ShoppingIcon },
  { href: "/plan", label: "Plan", icon: PlanIcon },
  { href: "/progress", label: "Progres", icon: ProgressIcon },
  { href: "/scan", label: "Skaner", icon: ScanIcon },
];

export function BottomNav() {
  const pathname = usePathname();

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 bg-background border-t border-border safe-area-bottom">
      <div className="flex justify-around items-center h-16 max-w-lg mx-auto">
        {tabs.map((tab) => {
          const isActive = tab.href === "/" ? pathname === "/" : pathname.startsWith(tab.href);
          return (
            <Link
              key={tab.href}
              href={tab.href}
              className={`flex flex-col items-center justify-center gap-0.5 px-3 py-2 rounded-lg transition-colors ${
                isActive
                  ? "text-green-600"
                  : "text-muted-foreground hover:text-foreground"
              }`}
            >
              <tab.icon active={isActive} />
              <span className="text-[10px] font-medium">{tab.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}

